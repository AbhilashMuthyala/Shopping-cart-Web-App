         var fadeIn_opacity = 0;
         var captions = new Array("Caribou Migration", "Caribou Crossing River", "Ceremonial Dress", "Family's Home ", "Scenic View", "Family Stove",
             "Summer Camp", "Wolf", "Winter Caribou Herd", "Spring Caribou");
       
         function reset() {
            
             fadeIn_opacity = 0;
             fadeOut_opacity = 1;
         }
         function Increment() {
           
             i++;
             if (i > 9) {
                 i = 0;
             }


             document.getElementById("imageView").src = pics[i];

             document.getElementById("imageText").innerHTML = captions[i];

         }
         function fadeIn() {
            
             //Set the initial opacity
             document.getElementById('imageView').style.opacity = fadeIn_opacity;

             //Increment by .001
             fadeIn_opacity += delta;

             if (fadeIn_opacity >= 1)
             {
                 reset();
                 fadeOut();
                 return;
             }
             //Set time out 
             timer = setTimeout("fadeIn()", 1);
            
         }

         function fadeOut()
         {
          

             document.getElementById('imageView').style.opacity = fadeOut_opacity;


             fadeOut_opacity -= delta;
             if (fadeOut_opacity <= 0)
             {
                 Increment();
                
                 fadeIn();
                 return;
                
             }
             timer = setTimeout("fadeOut()", 1);
         }
    
